# 1. Simple comparison
if 3 == 7:
    print("Equal")
else:
    print("Not equal")

# 2. Speed check
speed = 80
if speed > 100:
    print("Too fast")
else:
    print("Normal speed")

# 3. Password length check
password = "qwerty123"
if len(password) >= 8:
    print("Strong password")
else:
    print("Weak password")

# 4. List item check
items = [1, 2, 3]
if 5 in items:
    print("Item found")
else:
    print("Item not found")

# 5. Positive or Negative check
n = -3
if n >= 0:
    print("Non-negative")
else:
    print("Negative")
