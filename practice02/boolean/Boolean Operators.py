# 1. 'and' with multiple conditions
print(3 > 1 and 8 <= 10 and 4 != 0)  # True

# 2. 'or' with boolean variables
x, y = True, False
print(x or y)  # True

# 3. 'not' with comparison
print(not (7 == 7))  # False

# 4. Combining 'and' and 'or'
print((10 < 5 or 6 >= 6) and 2 + 2 == 4)  # True

# 5. 'not' with boolean variable
has_access = True
print(not has_access)  # False
